/**
 * Marching Cubes — SDF → triangle mesh.
 *
 * v1: Full-grid allocation, no vertex deduplication, no per-vertex normals.
 * Simple, correct, ~200 lines beyond lookup tables.
 *
 * Corner numbering (standard Bourke):
 *   4----5       z
 *  /|   /|       |  y
 * 7----6 |       | /
 * | 0--|-1       +--x
 * |/   |/
 * 3----2
 */

import type { SDF } from './sdf.js';
import type { Vec3, BoundingBox } from './vec3.js';
import type { TriangleMesh } from './mesh.js';

const MAX_VOXELS = 4_000_000;

/**
 * Extract a triangle mesh from an SDF via marching cubes.
 *
 * @param sdf       The signed distance field to mesh.
 * @param resolution Voxel size in mm (smaller = finer detail).
 * @param bounds    Optional bounding box override (defaults to sdf.bounds()).
 * @param padding   Extra padding around bounds (defaults to resolution * 2).
 */
export function marchingCubes(
  sdf: SDF,
  resolution = 2,
  bounds?: BoundingBox,
  padding?: number,
): TriangleMesh {
  if (resolution <= 0) throw new Error('Resolution must be positive');

  const pad = padding ?? resolution * 2;
  const b = bounds ?? sdf.bounds();
  const min: Vec3 = [b.min[0] - pad, b.min[1] - pad, b.min[2] - pad];
  const max: Vec3 = [b.max[0] + pad, b.max[1] + pad, b.max[2] + pad];

  const nx = Math.ceil((max[0] - min[0]) / resolution) + 1;
  const ny = Math.ceil((max[1] - min[1]) / resolution) + 1;
  const nz = Math.ceil((max[2] - min[2]) / resolution) + 1;

  const totalVoxels = nx * ny * nz;
  if (totalVoxels > MAX_VOXELS) {
    throw new Error(
      `Grid too large (${nx}×${ny}×${nz} = ${totalVoxels} voxels, max ${MAX_VOXELS}). ` +
      `Increase resolution (currently ${resolution}mm) or reduce bounds.`
    );
  }

  // Evaluate SDF at all grid points
  const grid = new Float64Array(totalVoxels);
  for (let iz = 0; iz < nz; iz++) {
    for (let iy = 0; iy < ny; iy++) {
      for (let ix = 0; ix < nx; ix++) {
        const p: Vec3 = [
          min[0] + ix * resolution,
          min[1] + iy * resolution,
          min[2] + iz * resolution,
        ];
        grid[ix + iy * nx + iz * nx * ny] = sdf.evaluate(p);
      }
    }
  }

  // March cubes
  const vertices: Vec3[] = [];
  const indices: number[] = [];

  for (let iz = 0; iz < nz - 1; iz++) {
    for (let iy = 0; iy < ny - 1; iy++) {
      for (let ix = 0; ix < nx - 1; ix++) {
        marchCube(ix, iy, iz, nx, ny, grid, min, resolution, vertices, indices);
      }
    }
  }

  // Compute mesh bounds from vertices
  let meshMin: Vec3 = [Infinity, Infinity, Infinity];
  let meshMax: Vec3 = [-Infinity, -Infinity, -Infinity];
  for (const v of vertices) {
    for (let i = 0; i < 3; i++) {
      if (v[i] < meshMin[i]) meshMin[i] = v[i];
      if (v[i] > meshMax[i]) meshMax[i] = v[i];
    }
  }
  if (vertices.length === 0) {
    meshMin = [0, 0, 0];
    meshMax = [0, 0, 0];
  }

  return {
    vertices,
    indices,
    vertexCount: vertices.length,
    triangleCount: indices.length / 3,
    bounds: { min: meshMin, max: meshMax },
  };
}

function marchCube(
  ix: number, iy: number, iz: number,
  nx: number, ny: number,
  grid: Float64Array,
  origin: Vec3,
  res: number,
  vertices: Vec3[],
  indices: number[],
): void {
  // 8 corner values
  const vals: number[] = [
    grid[ix     + iy       * nx + iz       * nx * ny], // 0
    grid[ix + 1 + iy       * nx + iz       * nx * ny], // 1
    grid[ix + 1 + (iy + 1) * nx + iz       * nx * ny], // 2
    grid[ix     + (iy + 1) * nx + iz       * nx * ny], // 3
    grid[ix     + iy       * nx + (iz + 1) * nx * ny], // 4
    grid[ix + 1 + iy       * nx + (iz + 1) * nx * ny], // 5
    grid[ix + 1 + (iy + 1) * nx + (iz + 1) * nx * ny], // 6
    grid[ix     + (iy + 1) * nx + (iz + 1) * nx * ny], // 7
  ];

  // Build cube index (bit i set if corner i is inside)
  let cubeIndex = 0;
  for (let i = 0; i < 8; i++) {
    if (vals[i] < 0) cubeIndex |= (1 << i);
  }

  const edges = EDGE_TABLE[cubeIndex];
  if (edges === 0) return;

  // 8 corner positions
  const x = origin[0] + ix * res;
  const y = origin[1] + iy * res;
  const z = origin[2] + iz * res;

  const corners: Vec3[] = [
    [x,       y,       z      ], // 0
    [x + res, y,       z      ], // 1
    [x + res, y + res, z      ], // 2
    [x,       y + res, z      ], // 3
    [x,       y,       z + res], // 4
    [x + res, y,       z + res], // 5
    [x + res, y + res, z + res], // 6
    [x,       y + res, z + res], // 7
  ];

  // Interpolate vertices on intersected edges
  const edgeVerts: Vec3[] = new Array(12);
  for (let e = 0; e < 12; e++) {
    if (edges & (1 << e)) {
      const [a, b] = EDGE_PAIRS[e];
      edgeVerts[e] = interpolate(corners[a], corners[b], vals[a], vals[b]);
    }
  }

  // Emit triangles
  const tris = TRI_TABLE[cubeIndex];
  for (let i = 0; tris[i] !== -1; i += 3) {
    const base = vertices.length;
    vertices.push(edgeVerts[tris[i]]);
    vertices.push(edgeVerts[tris[i + 1]]);
    vertices.push(edgeVerts[tris[i + 2]]);
    indices.push(base, base + 1, base + 2);
  }
}

/** Linear interpolation along an edge where the sign changes. */
function interpolate(p1: Vec3, p2: Vec3, v1: number, v2: number): Vec3 {
  if (Math.abs(v1) < 1e-10) return p1;
  if (Math.abs(v2) < 1e-10) return p2;
  const t = -v1 / (v2 - v1); // isolevel = 0
  return [
    p1[0] + t * (p2[0] - p1[0]),
    p1[1] + t * (p2[1] - p1[1]),
    p1[2] + t * (p2[2] - p1[2]),
  ];
}

